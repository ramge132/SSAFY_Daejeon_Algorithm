dxy = [[0, -1], [0, 1], [-1, 0], [1, 0]]


def bfs():
    while queue:
        cx, cy = queue.popleft()
        crash_times = maze[cy][cx][1]

        for dx, dy in dxy:
            nx, ny = cx + dx, cy + dy

            if maze[-1][-1][-1] == 0:
                return

            # 영역을 벗어남
            if not (0 <= nx <= M-1 and 0 <= ny <= N-1):
                continue

            # 벽인경우
            if maze[ny][nx][0] == 1:
                if maze[ny][nx][1] <= crash_times + 1:
                    continue

                maze[ny][nx][1] = crash_times+1
                queue.append((nx, ny))

            # 그냥 이동 가능한 경우
            else:
                if maze[ny][nx][1] <= crash_times:
                    continue

                maze[ny][nx][1] = crash_times
                queue.append((nx, ny))
    
